import type { Metadata } from "next";
import { Geist, Geist_Mono, Figtree } from "next/font/google";
import "./globals.css";

/**
 * Figtree font configuration for UI text elements
 *
 * Primary sans-serif font used for body text, buttons, and interface elements.
 * Optimized for readability at various sizes with excellent character distinction.
 * Applied globally via CSS variable --font-sans for consistent theming.
 *
 * Font characteristics:
 * - Humanist sans-serif design with open forms and friendly appearance
 * - Excellent legibility at small sizes for dense UI content
 * - Warm, approachable aesthetic suitable for creative applications
 * - Variable font format with weight axis (100-900) for typographic flexibility
 *
 * Usage:
 * - Body text, paragraphs, labels, and descriptive content
 * - UI controls (buttons, inputs, menus)
 * - Headings at smaller sizes (h3-h6)
 *
 * @see https://fonts.google.com/specimen/Figtree
 */
const figtree = Figtree({ subsets: ["latin"], variable: "--font-sans" });

/**
 * Geist Sans font configuration for primary interface text
 *
 * Clean, geometric sans-serif optimized for UI readability and information density.
 * Used as the primary interface font with excellent legibility at small sizes.
 * Applied globally via CSS variable --font-geist-sans.
 *
 * Font characteristics:
 * - Neutral, geometric sans-serif with high x-height
 * - Optimized for screen rendering and UI contexts
 * - Distinct character forms to prevent misreading (e.g., I/l/1)
 * - Variable font format with weight axis (100-900)
 *
 * Usage:
 * - Primary body text and interface copy
 * - Data displays and information-dense contexts
 * - Secondary headings and section titles
 *
 * @see https://vercel.com/font
 */
const geistSans = Geist({
    variable: "--font-geist-sans",
    subsets: ["latin"],
});

/**
 * Geist Mono font configuration for monospace contexts
 *
 * Monospaced variant of Geist designed for code, technical content, and tabular data.
 * Features consistent character width for alignment and technical readability.
 * Applied globally via CSS variable --font-geist-mono.
 *
 * Font characteristics:
 * - Monospaced design with programmer-friendly character distinctions
 * - Clear differentiation between similar characters (0/O, 1/l/I, {}[]())
 * - Optimized spacing for code readability and terminal environments
 * - Variable font format with weight axis (100-900)
 *
 * Usage:
 * - Code snippets and syntax highlighting
 * - Variable names and technical identifiers in UI
 * - Tabular data requiring character alignment
 * - Debug output and console displays
 *
 * @see https://vercel.com/font
 */
const geistMono = Geist_Mono({
    variable: "--font-geist-mono",
    subsets: ["latin"],
});

