/**
 * RootLayout component - Application root wrapper
 *
 * Serves as the foundational layout wrapper for the entire Next.js application.
 * Responsible for:
 * - Establishing global HTML document structure
 * - Injecting font configurations via CSS variables
 * - Applying global CSS styles and themes
 * - Setting language and base styling context
 * - Providing dark mode foundation via 'dark' class on body
 *
 * Architecture notes:
 * - Uses Next.js App Router layout convention (required file at app/layout.tsx)
 * - All page routes automatically inherit this layout structure
 * - Font variables injected via className prop on html/body elements
 * - Global styles imported from ./globals.css (resets, base styles, utilities)
 * - Dark mode enabled by default via 'dark' class on body element
 *
 * Font application strategy:
 * - figtree: Applied to html element as base sans-serif (--font-sans)
 * - geistSans: Available via --font-geist-sans CSS variable
 * - geistMono: Available via --font-geist-mono CSS variable for code contexts
 *
 * Theming:
 * - Dark mode foundation established via 'dark' class on body
 * - Light mode support possible via class toggling (not implemented in base layout)
 * - Color system defined in globals.css using CSS variables with dark/light variants
 *
 * Performance considerations:
 * - Font loading strategy handled automatically by Next.js Font Optimization
 * - Variable fonts reduce asset size compared to multiple static font files
 * - Critical CSS inlined via globals.css for fastest initial render
 *
 * @component
 * @param {Object} props - Component props
 * @param {React.ReactNode} props.children - Nested page content to render within layout
 * @returns {JSX.Element} Root HTML structure wrapping application content
 *
 * @example
 * // Automatic usage by Next.js App Router
 * // app/layout.tsx
 * export default function RootLayout({ children }) {
 *   return (
 *     <html lang="en">
 *       <body>{children}</body>
 *     </html>
 *   );
 * }
 *
 * @example
 * // Font usage in components via CSS variables
 * // components/CodeBlock.tsx
 * const CodeBlock = () => (
 *   <code className="font-mono font-geist-mono">
 *     local x = 10
 *   </code>
 * );
 */
export default function RootLayout({
    children,
}: Readonly<{
    children: React.ReactNode;
}>) {
    return (
        <html lang="en" className={figtree.variable}>
            <body
                className={`${geistSans.variable} ${geistMono.variable} antialiased dark`}
            >
                {children}
            </body>
        </html>
    );
}